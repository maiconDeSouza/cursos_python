# Operações aritméticas
print(f'soma -> 19 + 3 = {19 + 3}')
print(f'subtração -> 19 - 3 = {19 - 3}')
print(f'divisão -> 19 / 3 = {19 / 3}')
print(f'multiplicação -> 19 * 3 = {19 * 3}')
print(f'divisão inteira -> 19 // 3 = {19 // 3}')
print(f'exponenciação -> 19 ** 3 = {19 ** 3}')
print(f'modulo -> 19 % 3 = {19 % 3}')
