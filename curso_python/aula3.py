"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto (Conjunto de caracteres)
Strings são textos que estão dentro de aspas
"""

# Aspas Simples
print('Maicon Souza')
# Aspas duplas
print("São Paulo")
# Escape
print("Ele disse: \"Eu não bebo\"")

# r
# Para aparecer as barras, mas é usado em expressos reculares
print(r"Ele disse: \"Eu não bebo\"")

# Modo mais simples para usar as aspas
print('Ele disse: "Eu não bebo"')
